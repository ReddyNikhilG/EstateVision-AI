import numpy as np
import pandas as pd
import matplotlib.pyplot as plt
import seaborn as sns
import sklearn.datasets
from sklearn.model_selection import train_test_split
from xgboost import XGBRegressor
from sklearn import metrics

import matplotlib.patheffects as pe
from matplotlib.colors import LinearSegmentedColormap
from sklearn.metrics import r2_score, mean_squared_error

# loading the csv data to a Pandas DataFrame
house_price_dataset = pd.read_csv('/content/BostonHousing.csv')

# Printing the first 5 rows of the dataset
house_price_dataset.head()

# checking the number of rows & columns in the dataframe
house_price_dataset.shape

# checking the missing values
house_price_dataset.isnull().sum()

# statistical measures of dataset
house_price_dataset.describe()

"""Undetstanding the Correlation between Various features in the dataset

1.   Positive Correlation
2.   Negative Correlation
"""

correlation = house_price_dataset.corr()

# constructing a heatmap to understand the correlation


# ---- parameters to match the provided image ----
n = correlation.shape[0]

# custom diverging colormap similar to the image (blue -> light -> red)
colors = [
    (0.02, 0.09, 0.22),  # deep navy
    (0.15, 0.45, 0.85),  # blue
    (0.86, 0.94, 0.98),  # pale near-white center
    (0.98, 0.65, 0.58),  # warm coral
    (0.78, 0.06, 0.25)   # deep red
]
cmap = LinearSegmentedColormap.from_list("custom_div", colors, N=256)

# mask upper triangle (include diagonal blank to match your reference)
mask = np.triu(np.ones_like(correlation, dtype=bool), k=0)

# styling
plt.style.use("dark_background")
sns.set_context("notebook", font_scale=1.0)
fig, ax = plt.subplots(figsize=(10,10), facecolor="black")
fig.patch.set_facecolor("black")

# MAIN HEATMAP (lower triangle only)
heat = sns.heatmap(
    correlation,
    mask=mask,
    cmap=cmap,
    vmin=-1, vmax=1,
    center=0,
    square=True,
    linewidths=0.3,                # thin borders between colored cells
    linecolor=(1,1,1,0.04),
    cbar=True,
    cbar_kws={"shrink":0.78, "pad":0.03, "label":"Pearson r", "aspect":30},
    annot=True,
    fmt=".2f",
    annot_kws={"size":9, "weight":"bold", "va":"center"}
)

# Make axes truly black where masked cells are
ax.set_facecolor("black")

# TICK LABELS (rotate x labels to match screenshot)
ax.set_xticks(np.arange(n)+0.5)
ax.set_yticks(np.arange(n)+0.5)
ax.set_xticklabels(correlation.columns, rotation=65, ha="right", fontsize=10, family="DejaVu Sans")
ax.set_yticklabels(correlation.index, rotation=0, fontsize=10, family="DejaVu Sans")

# --- DOTTED GRID OVER WHOLE PLOT ---
# Minor ticks at cell boundaries so grid lines fall between squares
ax.set_xticks(np.arange(n+1), minor=True)
ax.set_yticks(np.arange(n+1), minor=True)

# fine dotted grid (visible across masked black area)
ax.grid(which='minor', color=(1,1,1,0.12), linestyle=':', linewidth=0.85)

# optionally draw slightly bolder white grid every `block` cells to create the boxed pattern
block = 3   # adjust to match how bold lines appear every few cells in your image
major_x = np.arange(0, n+1, block)
major_y = np.arange(0, n+1, block)
for mx in major_x:
    ax.axvline(mx, color=(1,1,1,0.18), linewidth=1.2, zorder=2)
for my in major_y:
    ax.axhline(my, color=(1,1,1,0.18), linewidth=1.2, zorder=2)

# remove tick marks for minor ticks
ax.tick_params(which='minor', bottom=False, left=False)

# annotation styling: white numbers with a subtle dark halo so they pop on neon tiles
for txt in ax.texts:
    txt.set_color("white")
    txt.set_fontsize(9)
    txt.set_weight("bold")
    txt.set_path_effects([
        pe.Stroke(linewidth=4, foreground='black', alpha=0.65),
        pe.Normal()
    ])

# TITLE with soft glow centered at top
title = ax.set_title("Correlation Matrix Heatmap", fontsize=18, pad=16, weight='semibold')
title.set_color("white")
title.set_path_effects([pe.Stroke(linewidth=6, foreground='black', alpha=0.7), pe.Normal()])

# Footer caption at bottom left (italic)
caption = ("This heatmap highlights key relationships (e.g., %ln strongly negative with bandgap). "
           "Masking the upper triangle reduces redundancy, making it neat and focused on unique correlations.")
fig.text(0.02, 0.02, caption, fontsize=9, color="white", style="italic", ha="left", va="bottom", wrap=True)

# colorbar styling to match: slimmer, ticks white
cbar = heat.collections[0].colorbar
cbar.ax.yaxis.set_tick_params(color='white', labelsize=9)
plt.setp(plt.getp(cbar.ax.axes, 'yticklabels'), color='white')
cbar.outline.set_edgecolor((1,1,1,0.06))
cbar.outline.set_linewidth(0.6)

# subtle spines
for spine in ax.spines.values():
    spine.set_color((1,1,1,0.05))
    spine.set_linewidth(0.6)

plt.tight_layout()
plt.savefig("replicated_research_heatmap.png", dpi=350, facecolor=fig.get_facecolor(), bbox_inches='tight')
plt.show()

"""Splitting the Data and Target"""

X = house_price_dataset.drop(['price'], axis=1)
Y = house_price_dataset['price']

print(X)
print(Y)

"""Splitting the data into Training data and Test data"""

X_train, X_test, Y_train, Y_test = train_test_split(X, Y, test_size=0.2, random_state=2)

print(X.shape, X_train.shape, X_test.shape)

"""Model Training

XGBoost Regressor
"""

# loading the model
model = XGBRegressor()

# training the modl with X_train
model.fit(X_train,Y_train)

"""Evaluation

Prediction on training data
"""

# accuracy for prediction on training data
training_data_prediction = model.predict(X_train)

print(training_data_prediction)

# R squared error
score_1 = metrics.r2_score(Y_train, training_data_prediction)

# Mean Absolute Error
score_2 = metrics.mean_absolute_error(Y_train, training_data_prediction)

print("R squared error : ", score_1)
print('Mean Absolute Error : ', score_2)

"""Visualizing the Actual Prices and Predicted Prices"""

# styling choices
plt.style.use("dark_background")
sns.set_context("notebook", font_scale=1.15)

fig, ax = plt.subplots(figsize=(10,5.5), facecolor="black")
fig.patch.set_facecolor("black")

# Scatter: neon cyan fill, white edge, slight transparency
ax.scatter(
    Y_train,
    training_data_prediction,
    s=70,                             # marker size
    facecolor="#68d1ff",              # neon-ish cyan fill
    edgecolor="white",                # crisp white rim
    linewidth=0.9,
    alpha=0.92,
    zorder=3
)

# Add subtle inner dot to create double-ring highlight (optional aesthetic)
ax.scatter(
    Y_train,
    training_data_prediction,
    s=18,
    facecolor="#aeefff",
    edgecolor="none",
    alpha=0.85,
    zorder=4
)

# Regression / identity line: dashed red fit line (and extend across plot)
# Fit a simple linear regression for trend
coeffs = np.polyfit(Y_train, training_data_prediction, 1)
poly = np.poly1d(coeffs)
x_line = np.linspace(min(Y_train), max(Y_train), 300)
y_line = poly(x_line)
# dashed fitted trend (slightly thicker, red)
line = ax.plot(x_line, y_line, linestyle='--', linewidth=2.0, color="#ff2e2e", zorder=2)[0]
# Add a faint glow to the red line to make it pop
line.set_path_effects([pe.Stroke(linewidth=6, foreground='black', alpha=0.55),
                       pe.Normal()])

# Optionally add identity y=x line for reference (thin, faint)
ax.plot(x_line, x_line, linestyle=':', linewidth=1.0, color=(1,1,1,0.12), zorder=1)

# Grid: minor grid lines dotted and visible over masked/empty areas
ax.set_xlim(min(Y_train) - 0.02*(max(Y_train)-min(Y_train)), max(Y_train) + 0.02*(max(Y_train)-min(Y_train)))
ax.set_ylim(min(training_data_prediction) - 0.02*(max(training_data_prediction)-min(training_data_prediction)),
            max(training_data_prediction) + 0.02*(max(training_data_prediction)-min(training_data_prediction)))

# Set major ticks at sensible intervals
ax.xaxis.set_major_locator(plt.MaxNLocator(6))
ax.yaxis.set_major_locator(plt.MaxNLocator(6))

# place minor ticks at cell boundaries and draw dotted grid
ax.set_xticks(np.linspace(ax.get_xlim()[0], ax.get_xlim()[1], 9), minor=True)
ax.set_yticks(np.linspace(ax.get_ylim()[0], ax.get_ylim()[1], 6), minor=True)
ax.grid(which='minor', linestyle=':', linewidth=0.85, color=(1,1,1,0.12))
ax.grid(which='major', linestyle='--', linewidth=0.7, color=(1,1,1,0.06))

# Labels & title: bold and large, matching the reference
ax.set_xlabel("Actual_formation", fontsize=18, fontweight='bold', labelpad=12, color='white')
ax.set_ylabel("Predicted_formation", fontsize=18, fontweight='bold', labelpad=12, color='white')
ax.set_title("Actual vs Predicted (Training)", fontsize=20, fontweight='semibold', pad=14, color='white')
ax.title.set_path_effects([pe.Stroke(linewidth=6, foreground='black', alpha=0.7), pe.Normal()])

# Tick label styling
ax.tick_params(axis='both', which='major', labelsize=13, colors='white')
for tl in (ax.get_xticklabels() + ax.get_yticklabels()):
    tl.set_fontfamily("DejaVu Sans")

# Stats box (R^2 and RMSE) — subtle white text with tiny halo
r2 = r2_score(Y_train, training_data_prediction)
mse = mean_squared_error(Y_train, training_data_prediction)
rmse = np.sqrt(mse)
stat_text = f"$R^2$ = {r2:.3f}\nRMSE = {rmse:.3f}"
txt = ax.text(0.02, 0.95, stat_text, transform=ax.transAxes,
              fontsize=12, color='white', va='top', ha='left',
              bbox=dict(boxstyle="round,pad=0.4", facecolor=(0,0,0,0.45), edgecolor=(1,1,1,0.08)))
txt.set_path_effects([pe.Stroke(linewidth=3, foreground='black', alpha=0.6), pe.Normal()])

# Tight layout & save/show
plt.tight_layout()
